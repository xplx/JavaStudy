package com.daoben.rfid.model;

/**
 * 地点实体类
 * 
 * @author hayypant 2017年4月19日
 *
 */
public class LocationInfo {

	private String location_id;
	private String asset_company;
	private String station_name;
	private String address;
	private String manager;
	private long phone;
	private String pid;
	
	public LocationInfo() {
		super();
	}

	public LocationInfo(String location_id, String asset_company, String station_name, String address, String manager,
			long phone, String pid) {
		super();
		this.location_id = location_id;
		this.asset_company = asset_company;
		this.station_name = station_name;
		this.address = address;
		this.manager = manager;
		this.phone = phone;
		this.pid = pid;
	}

	public String getLocation_id() {
		return location_id;
	}

	public void setLocation_id(String location_id) {
		this.location_id = location_id;
	}

	public String getAssetCompany() {
		return asset_company;
	}

	public void setAssetCompany(String assetCompany) {
		this.asset_company = assetCompany;
	}

	public String getStation_name() {
		return station_name;
	}

	public void setStation_name(String station_name) {
		this.station_name = station_name;
	}

	public String getAddress() {
		return address;
	}

	public void setAddress(String address) {
		this.address = address;
	}

	public String getManager() {
		return manager;
	}

	public void setManager(String manager) {
		this.manager = manager;
	}

	public long getPhone() {
		return phone;
	}

	public void setPhone(long phone) {
		this.phone = phone;
	}

	public String getPid() {
		return pid;
	}

	public void setPid(String pid) {
		this.pid = pid;
	}

	@Override
	public String toString() {
		return "LocationInfo [location_id=" + location_id + ", assetCompany=" + asset_company + ", station_name="
				+ station_name + ", address=" + address + ", manager=" + manager + ", phone=" + phone + ", pid=" + pid
				+ "]";
	}

}
